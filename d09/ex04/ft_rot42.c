#include <stdio.h>

char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 'z' && str[i] >= 'a')
		{
			str[i] -= 'a';
			str[i] += 42;
			str[i] %= 26;
			str[i] += 'a';
		}
		else if (str[i] <= 'Z' && str[i] >= 'A')
		{
			str[i] -= 'A';
			str[i] += 42;
			str[i] %= 26;
			str[i] += 'A';
		}
		++i;
	}
	return (str);
}
