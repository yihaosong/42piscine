int	ft_is_prime(int nb)
{
	int i;
	int j;

	i = 3;
	j = 0;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 0)
		return (0);
	while (j * j < nb && j * j >= 0)
		++j;
	if (j * j == nb)
		return (0);
	while (i < j)
	{
		if (nb % i == 0)
			return (0);
		i = i + 2;
	}
	return (1);
}
