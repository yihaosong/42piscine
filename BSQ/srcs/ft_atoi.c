int		ft_atoi(char *str)
{
	int i;
	int neg;
	int out;

	i = 0;
	neg = 1;
	out = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		out = out * 10 + str[i] - 48;
		i++;
	}
	return (out * neg);
}
